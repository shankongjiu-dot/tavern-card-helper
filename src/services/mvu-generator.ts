// ── Full MVU asset generation ────────────────────────────────────────────

/**
 * Generate all MVU assets from an MvuConfig.
 * Returns the updated config with generated asset text populated.
 */
export function generateAllMvuAssets(config: MvuConfig): MvuConfig {
  if (!config.enabled || config.variables.length === 0) return config;

  return {
    ...config,
    schemaJs: buildSchemaJs(config.variables),
    initvarYaml: buildInitvarYaml(config.variables),
    updateRulesYaml: buildUpdateRules(config.variables),
    variableListMd: buildVariableList(config.variables),
    outputFormatMd: buildOutputFormat(config.variables),
    // Generate status bar if enabled
    statusBarHtml: config.statusBarEnabled
      ? buildStatusBarHtml(config.variables, config.statusBarMode)
      : config.statusBarHtml,
    statusBarCss: config.statusBarEnabled && config.statusBarMode === 'safe_macro'
      ? buildStatusBarCss()
      : config.statusBarCss,
    // Generate story beautification if enabled
    storyBeautifyHtml: config.storyBeautifyEnabled
      ? buildStoryBeautifyHtml(config.storyBeautifyTag || 'story_view')
      : config.storyBeautifyHtml,
  };
}

/**
 * Package all MVU assets as downloadable files.
 * Returns an array of { filename, content } pairs.
 */
export function packageMvuAssets(config: MvuConfig): Array<{ filename: string; content: string }> {
  const files: Array<{ filename: string; content: string }> = [];

  if (config.enabled && config.variables.length > 0) {
    files.push({ filename: 'mvu/schema.js', content: config.schemaJs });
    files.push({ filename: 'mvu/initvar.yaml', content: config.initvarYaml });
    files.push({ filename: 'mvu/update-rules.yaml', content: config.updateRulesYaml });
    files.push({ filename: 'mvu/variable-list.md', content: config.variableListMd });
    files.push({ filename: 'mvu/output-format.md', content: config.outputFormatMd });
  }

  if (config.statusBarEnabled) {
    if (config.statusBarMode === 'safe_macro') {
      files.push({ filename: 'html/statusbar.html', content: config.statusBarHtml });
      files.push({ filename: 'html/statusbar.css', content: config.statusBarCss });
    } else {
      files.push({ filename: 'html/statusbar.html', content: config.statusBarHtml });
    }
  }

  if (config.storyBeautifyEnabled && config.storyBeautifyHtml) {
    files.push({ filename: `html/${config.storyBeautifyTag || 'story-view'}.html`, content: config.storyBeautifyHtml });
  }

  // Always include MVU update beautification when MVU is enabled
  if (config.enabled && config.variables.length > 0) {
    files.push({ filename: 'html/变量更新中美化.html', content: buildMvuLoadingBeautifyHtml() });
    files.push({ filename: 'html/变量更新美化.html', content: buildMvuDoneBeautifyHtml() });
  }

  return files;
}

/**
 * Download all MVU assets as individual files.
 */
export function downloadMvuAssets(config: MvuConfig): void {
  const files = packageMvuAssets(config);
  if (files.length === 0) return;

  // Create a simple text bundle for convenience
  const bundle = files.map(f => `// ═══ ${f.filename} ═══\n${f.content}`).join('\n\n');
  const blob = new Blob([bundle], { type: 'text/plain;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = 'mvu-assets.txt';
  a.click();
  URL.revokeObjectURL(url);
}

// ── Utility helpers ───────────────────────────────────────────────────────

function defaultForKind(kind?: string): unknown {
  if (kind === 'number') return 0;
  if (kind === 'boolean') return false;
  if (kind === 'object' || kind === 'record') return {};
  return '';
}

function setNested(root: Record<string, unknown>, segments: string[], value: unknown): void {
  let cursor = root;
  for (let i = 0; i < segments.length; i++) {
    if (i === segments.length - 1) {
      cursor[segments[i]] = value;
    } else {
      const existing = cursor[segments[i]];
      if (!existing || typeof existing !== 'object') {
        cursor[segments[i]] = {};
      }
      cursor = cursor[segments[i]] as Record<string, unknown>;
    }
  }
}

/** Simple YAML stringifier for nested objects */
function yamlStringify(obj: Record<string, unknown>, indent = 0): string {
  const prefix = '  '.repeat(indent);
  const lines: string[] = [];
  for (const [key, value] of Object.entries(obj)) {
    if (value === null || value === undefined) {
      lines.push(`${prefix}${key}: null`);
    } else if (typeof value === 'object' && !Array.isArray(value)) {
      lines.push(`${prefix}${key}:`);
      lines.push(yamlStringify(value as Record<string, unknown>, indent + 1));
    } else if (Array.isArray(value)) {
      lines.push(`${prefix}${key}:`);
      for (const item of value) {
        lines.push(`${prefix}  - ${yamlScalar(item)}`);
      }
    } else {
      lines.push(`${prefix}${key}: ${yamlScalar(value)}`);
    }
  }
  return lines.join('\n');
}

function yamlScalar(value: unknown): string {
  if (typeof value === 'string') {
    // Quote strings that could be misinterpreted
    if (/^(true|false|null|yes|no|on|off|\d)/i.test(value) || value.includes(':') || value.includes('#') || value.includes('\n')) {
      return `"${value.replace(/"/g, '\\"')}"`;
    }
    return value;
  }
  return String(value);
}
