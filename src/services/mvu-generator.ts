/**
 * MVU Generator - generates MVU (Model-View-Update) five-piece set assets.
 * Based on world-book-mcp v5 methodology.
 *
 * Five files:
 *   1. schema.js     - Zod schema defining variable structure
 *   2. initvar.yaml  - Initial variable values
 *   3. update-rules.yaml - When/how variables update
 *   4. variable-list.md  - Variable documentation
 *   5. output-format.md  - AI output format for variable updates
 *
 * Also generates optional frontend beautification assets:
 *   - Status bar HTML/CSS
 *   - Story view beautification HTML
 *   - MVU update visualization HTML
 */
import type { MvuVariable, MvuConfig } from '../constants/defaults';

// ── Schema.js builder ─────────────────────────────────────────────────────

/**
 * Build schema.js content from MVU variables.
 * Uses Zod 4 syntax as per world-book-mcp v5 spec.
 */
export function buildSchemaJs(variables: MvuVariable[]): string {
  const lines = [
    '// Generated by Tavern Card Helper — MVU schema',
    '// Runtime provides z (Zod) and _ (lodash with .clamp)',
    'export const Schema = z.object({',
  ];

  const tree = buildVariableTree(variables);
  lines.push(...renderSchemaTree(tree, 1));
  lines.push('});', '');
  return lines.join('\n');
}

interface VariableTree {
  [key: string]: VariableTree | MvuVariable;
}

function buildVariableTree(variables: MvuVariable[]): VariableTree {
  const root: VariableTree = {};
  for (const v of variables) {
    let cursor = root;
    for (let i = 0; i < v.path.length; i++) {
      const seg = v.path[i];
      if (i === v.path.length - 1) {
        cursor[seg] = v;
      } else {
        if (!cursor[seg] || isMvuVariable(cursor[seg])) {
          cursor[seg] = {} as VariableTree;
        }
        cursor = cursor[seg] as VariableTree;
      }
    }
  }
  return root;
}

function isMvuVariable(value: unknown): value is MvuVariable {
  return typeof value === 'object' && value !== null && 'path' in value && Array.isArray((value as MvuVariable).path);
}

function renderSchemaTree(tree: VariableTree, depth: number): string[] {
  const indent = '  '.repeat(depth);
  const lines: string[] = [];
  for (const [key, value] of Object.entries(tree)) {
    if (isMvuVariable(value)) {
      lines.push(`${indent}${JSON.stringify(key)}: ${schemaExpression(value)},`);
    } else {
      lines.push(`${indent}${JSON.stringify(key)}: z.object({`);
      lines.push(...renderSchemaTree(value, depth + 1));
      lines.push(`${indent}}),`);
    }
  }
  return lines;
}

function schemaExpression(v: MvuVariable): string {
  switch (v.kind) {
    case 'number': {
      const hasRange = typeof v.min === 'number' || typeof v.max === 'number';
      const base = 'z.coerce.number()';
      if (hasRange) {
        return `${base}.transform(v => _.clamp(v, ${v.min ?? '-Infinity'}, ${v.max ?? 'Infinity'}))`;
      }
      return base;
    }
    case 'boolean':
      return 'z.boolean()';
    case 'enum':
      if (v.enumValues && v.enumValues.length > 0) {
        return `z.enum([${v.enumValues.map(e => JSON.stringify(e)).join(', ')}])`;
      }
      return 'z.string()';
    case 'record':
      return 'z.record(z.string(), z.unknown())';
    case 'object':
      return 'z.object({})';
    default:
      return 'z.string()';
  }
}

// ── initvar.yaml builder ──────────────────────────────────────────────────

/**
 * Build initvar.yaml content from MVU variables.
 * Generates nested YAML structure with default values.
 */
export function buildInitvarYaml(variables: MvuVariable[]): string {
  const root: Record<string, unknown> = {};
  for (const v of variables) {
    setNested(root, v.path, v.defaultValue ?? defaultForKind(v.kind));
  }
  return yamlStringify(root) + '\n';
}

// ── update-rules.yaml builder ─────────────────────────────────────────────

/**
 * Build update-rules.yaml content from MVU variables.
 * Skips hidden (_) and readonly ($) variables per spec.
 */
export function buildUpdateRules(variables: MvuVariable[]): string {
  const rules: Record<string, unknown> = {};
  for (const v of variables) {
    if (shouldSkipUpdateRule(v)) continue;
    const rule: Record<string, unknown> = {};

    if (v.kind === 'number') {
      rule.type = 'number';
      if (typeof v.min === 'number' || typeof v.max === 'number') {
        rule.range = `${v.min ?? '-Infinity'}~${v.max ?? 'Infinity'}`;
      }
    } else if (v.kind === 'boolean') {
      rule.type = 'boolean';
    } else if (v.kind === 'enum' && v.enumValues?.length) {
      rule.type = v.enumValues.map(e => `'${e}'`).join('|');
    } else if (v.kind === 'record') {
      rule.type = '{ [key: string]: unknown }';
    }

    if (v.description) rule.value = v.description;
    if (Object.keys(rule).length > 0) setNested(rules, v.path, rule);
  }
  return yamlStringify({ 变量更新规则: rules });
}

function shouldSkipUpdateRule(v: MvuVariable): boolean {
  const last = v.path.at(-1) ?? '';
  return Boolean(
    v.readonly || v.hidden ||
    last.startsWith('_') || last.startsWith('$') ||
    v.path.some(seg => seg.startsWith('_') || seg.startsWith('$'))
  );
}

// ── variable-list.md builder ──────────────────────────────────────────────

/**
 * Build variable-list.md content.
 * Standard format with current variables block and descriptions.
 */
export function buildVariableList(variables: MvuVariable[]): string {
  const descriptions = variables
    .map(v => {
      const dotPath = v.path.join('.');
      return `- stat_data.${dotPath}${v.description ? `：${v.description}` : ''}`;
    })
    .join('\n');

  return [
    '---',
    '<status_current_variables>',
    '{{format_message_variable::stat_data}}',
    '</status_current_variables>',
    '',
    '# 变量说明',
    descriptions,
    '',
  ].join('\n');
}

// ── output-format.md builder ──────────────────────────────────────────────

/**
 * Build output-format.md content.
 * Defines how AI should output variable updates using JSON Patch format.
 */
export function buildOutputFormat(variables: MvuVariable[]): string {
  const examples = variables
    .filter(v => !shouldSkipUpdateRule(v))
    .map(v => {
      const path = '/' + v.path.join('/');
      const value = JSON.stringify(v.defaultValue ?? defaultForKind(v.kind));
      return `      { "op": "replace", "path": "${path}", "value": ${value} }`;
    })
    .join(',\n');

  const trackedVars = variables
    .map(v => `    - stat_data.${v.path.join('.')}`)
    .join('\n');

  return [
    '---',
    '变量输出格式:',
    '  rule:',
    '    - 在下一次回复末尾同时输出更新分析和实际更新命令。',
    '    - 更新命令必须是 JSON Patch 风格的 JSON 数组；路径不带 stat_data 根键。',
    '    - 支持 replace、delta、insert、remove、move；不要更新字段名以 _ 或 $ 开头的变量。',
    '  tracked_variables:',
    trackedVars,
    '  format: |-',
    '    <UpdateVariable>',
    '    <Analysis>$(IN ENGLISH, no more than 80 words; analyze only the current reply.)</Analysis>',
    '    <JSONPatch>',
    '    [',
    examples || '      { "op": "replace", "path": "/变量路径", "value": "新值" }',
    '    ]',
    '    </JSONPatch>',
    '    </UpdateVariable>',
    '',
  ].join('\n');
}

// ── Status bar HTML/CSS builder ───────────────────────────────────────────

/**
 * Generate status bar HTML based on MVU variables.
 * Uses safe_macro mode with format_message_variable.
 */
export function buildStatusBarHtml(variables: MvuVariable[], mode: 'safe_macro' | 'dynamic_js'): string {
  // Filter to displayable variables (not hidden, not readonly internal)
  const displayVars = variables.filter(v => !v.hidden && !v.path.some(s => s.startsWith('$')));

  if (mode === 'dynamic_js') {
    return buildDynamicStatusBarHtml(displayVars);
  }

  // safe_macro mode
  const rows = displayVars.map(v => {
    const dotPath = `stat_data.${v.path.join('.')}`;
    const label = v.path.at(-1) ?? v.path.join('.');
    return `    <div class="status-row">
      <span class="status-label">${label}</span>
      <strong class="status-value">{{format_message_variable::${dotPath}}}</strong>
    </div>`;
  }).join('\n');

  return `<div class="mvu-status-card">
  <div class="status-header">状态面板</div>
${rows}
</div>`;
}

function buildDynamicStatusBarHtml(variables: MvuVariable[]): string {
  const ids = variables.map(v => ({
    id: `mvu-${v.path.join('-')}`,
    label: v.path.at(-1) ?? v.path.join('.'),
    jsPath: v.path.join('.'),
    defaultVal: v.defaultValue ?? defaultForKind(v.kind),
  }));

  const rows = ids.map(item =>
    `    <div class="status-row">
      <span class="status-label">${item.label}</span>
      <strong class="status-value" id="${item.id}">${String(item.defaultVal)}</strong>
    </div>`
  ).join('\n');

  const updates = ids.map(item =>
    `      $('#${item.id}').text(_.get(statData, '${item.jsPath}', '${item.defaultVal}'));`
  ).join('\n');

  return `<!doctype html>
<html lang="zh-CN">
<head>
  <style>
  .mvu-status-card {
    width: 92%; margin: 12px auto; padding: 12px 14px;
    border: 1px solid rgba(148, 163, 184, .28); border-radius: 14px;
    background: rgba(15, 23, 42, .72); color: #e2e8f0;
  }
  .status-header { font-size: 13px; font-weight: 600; margin-bottom: 8px; opacity: .7; }
  .status-row { display: flex; justify-content: space-between; gap: 12px; padding: 4px 0; }
  .status-label { font-size: 12px; color: #94a3b8; }
  .status-value { font-size: 13px; color: #e2e8f0; }
  </style>
  <script type="module">
    async function initStatusBar() {
      await waitGlobalInitialized('Mvu');
      function render() {
        const allVariables = getAllVariables();
        const statData = _.get(allVariables, 'stat_data', {});
${updates}
      }
      render();
      if (typeof Mvu !== 'undefined' && Mvu.events) {
        eventOn(Mvu.events.VARIABLE_UPDATE_ENDED, render);
      }
    }
    $(errorCatched(initStatusBar));
  </script>
</head>
<body>
  <div class="mvu-status-card">
    <div class="status-header">状态面板</div>
${rows}
  </div>
</body>
</html>`;
}

